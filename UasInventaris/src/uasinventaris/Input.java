    private void tabelbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelbarangMouseClicked
        // TODO add your handling code here:
        int baris = tabelbarang.rowAtPoint(evt.getPoint());
        String kod = tabelbarang.getValueAt(baris, 1).toString();
        Kode_barang.setText(kod);
        
        String nama = tabelbarang.getValueAt(baris, 2).toString();
        Nama_barang.setText(nama);
        
        String jml = tabelbarang.getValueAt(baris, 3).toString();
        Jumlah_barang.setText(jml);
        
        String asal = tabelbarang.getValueAt(baris, 4).toString();
        Asal_barang.setSelectedItem(asal);
        
        String tgl = tabelbarang.getValueAt(baris, 4).toString();
        Tanggal_masuk.setText(tgl);
        
    }//GEN-LAST:event_tabelbarangMouseClicked

